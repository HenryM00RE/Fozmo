import { createHash } from 'node:crypto';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname } from 'node:path';
import { compileFromFile } from 'json-schema-to-typescript';

const [schemaPath, outputPath, endpointsOutputPath] = process.argv.slice(2);

if (!schemaPath || !outputPath || !endpointsOutputPath) {
  console.error(
    'usage: node ui/scripts/generate-api-types.mjs <schema.json> <api-types.ts> <api-endpoints.ts>'
  );
  process.exit(2);
}

const schemaText = await readFile(schemaPath, 'utf8');
const schema = JSON.parse(schemaText);
const schemaHash = createHash('sha256').update(schemaText).digest('hex');
const generated = await compileFromFile(schemaPath, {
  bannerComment: '',
  cwd: process.cwd(),
  style: {
    bracketSpacing: true,
    printWidth: 100,
    semi: true,
    singleQuote: true,
    tabWidth: 2,
    trailingComma: 'none',
    useTabs: false
  }
});

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(
  outputPath,
  [
    '// Generated by `cargo run --release --bin generate_api_contracts`. Do not edit.',
    `// schema-sha256: ${schemaHash}`,
    generated.trimEnd(),
    ''
  ].join('\n')
);

const endpointContracts = schema['x-fozmo-endpoints'] || [];
const responseSchemas = [
  ...new Set(endpointContracts.map((contract) => contract.response_schema).filter(Boolean))
].sort();
const requestSchemas = [
  ...new Set(endpointContracts.map((contract) => contract.request_schema).filter(Boolean))
].sort();
const querySchemas = [
  ...new Set(endpointContracts.map((contract) => contract.query_schema).filter(Boolean))
].sort();

function tsTypeForSchema(schemaName) {
  if (schemaName === 'JsonRecord') return 'UnsafeJsonRecord';
  if (schemaName.endsWith('[]')) return `${tsTypeForSchema(schemaName.slice(0, -2))}[]`;
  return `ApiTypes.${schemaName.replaceAll('_for_', 'For_')}`;
}

function methodName(contract) {
  const parts = contract.path
    .split('/')
    .filter(Boolean)
    .filter((part) => part !== 'api')
    .map((part) => {
      if (part === '*') return 'Wildcard';
      const clean = part.startsWith(':') ? `by-${part.slice(1)}` : part;
      return clean
        .split(/[-_]/)
        .filter(Boolean)
        .map((word) => `${word[0]?.toUpperCase() || ''}${word.slice(1)}`)
        .join('');
    });
  const prefix = contract.method.toLowerCase();
  return `${prefix}${parts.join('') || 'Root'}`;
}

function pathParamType(contract) {
  const params = contract.path_params || [];
  if (!params.length) return null;
  return `{ ${params.map((param) => `${param}: string | number`).join('; ')} }`;
}

function pathExpression(contract) {
  const params = contract.path_params || [];
  if (!params.length) return `'${contract.path}'`;
  return params.reduce(
    (source, param) =>
      source.replaceAll(`:${param}`, `\${encodeURIComponent(String(pathParams.${param}))}`),
    `\`${contract.path}\``
  );
}

function functionArgs(contract) {
  const args = [];
  const params = pathParamType(contract);
  if (params) args.push(`pathParams: ${params}`);
  if (contract.query_schema) {
    args.push(`query?: ContractQuery<'${contract.method}', '${contract.path}'>`);
  }
  if (contract.request_schema) {
    args.push(`body: ContractRequest<'${contract.method}', '${contract.path}'>`);
  }
  args.push('options: ContractRequestOptions = {}');
  return args.join(', ');
}

function functionBody(contract) {
  const path = pathExpression(contract);
  const method = contract.method === 'GET' ? '' : `method: '${contract.method}', `;
  const body = contract.request_schema ? 'body, ' : '';
  const queryPath = contract.query_schema ? `withQuery(${path}, query)` : path;
  return `transport.request<ContractResponse<'${contract.method}', '${contract.path}'>>(${queryPath}, { ${method}${body}signal: options.signal, silentStatuses: options.silentStatuses })`;
}

const endpointMethods = endpointContracts.map(
  (contract) =>
    `  ${methodName(contract)}: (${functionArgs(contract)}) => ${functionBody(contract)}`
);

const endpointSource = [
  '// Generated by `cargo run --release --bin generate_api_contracts`. Do not edit.',
  `// schema-sha256: ${schemaHash}`,
  "import type * as ApiTypes from './api-types';",
  '',
  'declare const unsafeJsonRecordBrand: unique symbol;',
  "export type UnsafeJsonRecord = Record<string, unknown> & { readonly [unsafeJsonRecordBrand]: 'loose-endpoint' };",
  '',
  `export const endpointContracts = ${JSON.stringify(endpointContracts, null, 2)} as const;`,
  '',
  'export type EndpointContract = (typeof endpointContracts)[number];',
  "export type EndpointKey = `${EndpointContract['method']} ${EndpointContract['path']}`;",
  "export type ContractMethod = EndpointContract['method'];",
  'export type ContractPath<M extends ContractMethod = ContractMethod> = Extract<',
  '  EndpointContract,',
  '  { method: M }',
  ">['path'];",
  '',
  'export interface ContractResponseBySchema {',
  ...responseSchemas.map((schemaName) => `  '${schemaName}': ${tsTypeForSchema(schemaName)};`),
  '}',
  '',
  'export interface ContractRequestBySchema {',
  ...requestSchemas.map((schemaName) => `  '${schemaName}': ${tsTypeForSchema(schemaName)};`),
  '}',
  '',
  'export interface ContractQueryBySchema {',
  ...querySchemas.map((schemaName) => `  '${schemaName}': ${tsTypeForSchema(schemaName)};`),
  '}',
  '',
  'export type ContractResponse<',
  '  M extends ContractMethod,',
  '  P extends ContractPath<M>',
  '> = Extract<EndpointContract, { method: M; path: P }> extends {',
  '  response_schema: infer SchemaName extends keyof ContractResponseBySchema;',
  '}',
  '  ? ContractResponseBySchema[SchemaName]',
  '  : never;',
  '',
  'export type ContractRequest<',
  '  M extends ContractMethod,',
  '  P extends ContractPath<M>',
  '> = Extract<EndpointContract, { method: M; path: P }> extends {',
  '  request_schema: infer SchemaName extends keyof ContractRequestBySchema;',
  '}',
  '  ? ContractRequestBySchema[SchemaName]',
  '  : never;',
  '',
  'export type ContractQuery<',
  '  M extends ContractMethod,',
  '  P extends ContractPath<M>',
  '> = Extract<EndpointContract, { method: M; path: P }> extends {',
  '  query_schema: infer SchemaName extends keyof ContractQueryBySchema;',
  '}',
  '  ? ContractQueryBySchema[SchemaName]',
  '  : never;',
  '',
  'export type ContractQueryParams = Record<string, unknown>;',
  '',
  'export interface ContractRequestOptions {',
  '  signal?: AbortSignal;',
  '  silentStatuses?: number[];',
  '}',
  '',
  'export interface ContractTransport {',
  '  request<T>(',
  '    path: string,',
  "    options?: { method?: ContractMethod; body?: unknown; signal?: AbortSignal; silentStatuses?: number[] }",
  '  ): Promise<T>;',
  '}',
  '',
  'function withQuery(path: string, params?: ContractQueryParams) {',
  '  if (!params) return path;',
  '  const url = new URL(path, window.location.origin);',
  '  Object.entries(params).forEach(([key, value]) => {',
  "    if (value !== null && value !== undefined && value !== '' && (typeof value === 'string' || typeof value === 'number' || typeof value === 'boolean')) {",
  '      url.searchParams.set(key, String(value));',
  '    }',
  '  });',
  '  return `${url.pathname}${url.search}${url.hash}`;',
  '}',
  '',
  'export function createApiClient(transport: ContractTransport) {',
  '  return {',
  endpointMethods.join(',\n'),
  '  };',
  '}',
  ''
].join('\n');

await mkdir(dirname(endpointsOutputPath), { recursive: true });
await writeFile(endpointsOutputPath, endpointSource);
